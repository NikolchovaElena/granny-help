package com.example.granny.domain.models.view;

public class LocationViewModel {

    Integer id;
    String name;

    public LocationViewModel() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
